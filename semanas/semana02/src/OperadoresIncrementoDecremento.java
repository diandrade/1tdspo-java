public class OperadoresIncrementoDecremento {

    public static void main(String[] args) {
        int valor1 = 1;

        System.out.println("valor1 = " + valor1);

        //Incrementar (somar uma unidade)
        valor1++;
        System.out.println("valor1 = " + valor1);
        
        //Decrementar (subtrair uma unidade)
        valor1--;
        System.out.println("valor1 = " + valor1);
    }
}
